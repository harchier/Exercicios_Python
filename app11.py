# Operações com inteiros e reais

num1 = int(input("Digite o primeiro número(inteiro):"))
num2 = int(input("Digite o segundo número(inteiro):"))
num3 = float(input("digite o terceiro número(real):"))

# a) o produto do dobro do primeiro com metade do segundo.

letra_a = (num1 * 2) * (num2 / 2)

print(f"O produto do dobro do primeiro com metade do segundo é {letra_a}")

# b) a soma do triplo do primeiro com o terceiro.

letra_b = (num1 * 3) + num3

print(f"A soma do triplo do primeiro com o terceiro é {letra_b}")

# c) o terceiro elevado ao cubo.

letra_c = num3 ** 3

print(f"O terceiro elevado ao cubo é {letra_c}")